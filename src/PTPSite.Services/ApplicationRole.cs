namespace PTPSite.Services
{
	/// <summary>
	/// Enumeration of application user roles.
	/// </summary>
	public enum ApplicationRole : int
	{
		/// <summary>
		/// Flag indicating user has no role.
		/// </summary>
		None = 0,

		/// <summary>
		/// Flag indicating user is a administrator.
		/// </summary>
		Administrator = 1,
	}
}
